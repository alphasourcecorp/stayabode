package stayabode.foodyHive.models;

public class ChefCard {
    String newOrdersCount;
    String currentOrdersCount;
    String subscriptionsCount;

    public String getNewOrdersCount() {
        return newOrdersCount;
    }

    public void setNewOrdersCount(String newOrdersCount) {
        this.newOrdersCount = newOrdersCount;
    }

    public String getCurrentOrdersCount() {
        return currentOrdersCount;
    }

    public void setCurrentOrdersCount(String currentOrdersCount) {
        this.currentOrdersCount = currentOrdersCount;
    }

    public String getSubscriptionsCount() {
        return subscriptionsCount;
    }

    public void setSubscriptionsCount(String subscriptionsCount) {
        this.subscriptionsCount = subscriptionsCount;
    }
}
