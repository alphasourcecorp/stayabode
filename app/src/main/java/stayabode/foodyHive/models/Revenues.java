package stayabode.foodyHive.models;

public class Revenues {
    String monthName;
    String yearAmount;

    String twentyYearAmount;

    public String getMonthName() {
        return monthName;
    }

    public void setMonthName(String monthName) {
        this.monthName = monthName;
    }

    public String getYearAmount() {
        return yearAmount;
    }

    public void setYearAmount(String yearAmount) {
        this.yearAmount = yearAmount;
    }

    public String getTwentyYearAmount() {
        return twentyYearAmount;
    }

    public void setTwentyYearAmount(String twentyYearAmount) {
        this.twentyYearAmount = twentyYearAmount;
    }

    String nineteenYear;

    public String getNineteenYear() {
        return nineteenYear;
    }

    public void setNineteenYear(String nineteenYear) {
        this.nineteenYear = nineteenYear;
    }

    public String getTwentyYear() {
        return twentyYear;
    }

    public void setTwentyYear(String twentyYear) {
        this.twentyYear = twentyYear;
    }

    String twentyYear;
}
